package recrypt

import "testing"

func TestRecrypt(t *testing.T) {
	api := NewApi256()

	keys := api.generateKeyPair()
	signingKeys := api.generateEd25519KeyPair()
	plaintext := api.generatePlaintext()
	encryptedValue := api.encrypt(plaintext, keys.publicKey, signingKeys.privateKey)
	decryptedValue := api.decrypt(encryptedValue, keys.privateKey)

	t.Log(keys)
	t.Log(signingKeys)
	t.Log(plaintext)
	t.Log(encryptedValue)
	t.Log(decryptedValue)
}
